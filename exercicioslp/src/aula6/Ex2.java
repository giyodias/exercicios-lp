package aula6;

import java.util.Scanner;

public class Ex2 {

	public static void main(String[] args) {

		Scanner leia = new Scanner(System.in);
		
		System.out.println("Digite um número: ");
		float num1 = leia.nextFloat();
		
		System.out.println("Digite um número novamente: ");
		float num2 = leia.nextFloat();
		
		calcularSoma(num1, num2);
		
		leia.close();
		
	}

	public static void calcularSoma(float valor1, float valor2) {
		
		float soma = valor1 + valor2;
		
		System.out.println("O resultado da soma dos números é: " + soma);
		
		
	}
}
